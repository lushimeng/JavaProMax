package it.heima.d13_extends_constructor2;

public class People {
    private String name;
    private int age;

    public People(){}  // 无参构造器

    public People(String name, int age){  // 有参构造器
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
